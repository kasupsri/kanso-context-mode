#!/usr/bin/env tsx
import { writeFileSync } from 'fs';
import { compress } from '../src/compression/strategies.js';

const fixtures = [
  {
    name: 'Large JSON',
    input: JSON.stringify(Array.from({ length: 500 }, (_, i) => ({ id: i, value: `item-${i}`, flags: ['alpha', 'beta', 'gamma'] }))),
  },
  {
    name: 'Large Log',
    input:
      Array.from({ length: 1000 }, (_, i) => `2026-03-16T10:00:${String(i % 60).padStart(2, '0')}Z INFO request ${i}`).join('\n') +
      '\n2026-03-16T10:02:00Z ERROR database timeout',
  },
  {
    name: 'Git Diff',
    input: [
      'diff --git a/src/app.ts b/src/app.ts',
      '@@ -1,0 +1,6 @@',
      '+export function main() {',
      '+  console.log("hello");',
      '+}',
      'diff --git a/src/cache.ts b/src/cache.ts',
      '@@ -10,2 +10,5 @@',
      '-const oldCache = true;',
      '+const hotCache = true;',
      '+export const HOT_CACHE_LIMIT = 32;',
    ].join('\n'),
  },
];

const rows = fixtures.map(fixture => {
  const result = compress(fixture.input, { maxOutputChars: 2048, strategy: 'ultra' });
  return {
    name: fixture.name,
    type: result.contentType,
    input: fixture.input.length,
    output: result.output.length,
    saved: result.savedPercent,
    strategy: result.strategy,
  };
});

const markdown = [
  '# Kanso Compression Benchmarks',
  '',
  '| Fixture | Type | Input Bytes | Output Bytes | Saved | Strategy |',
  '|---|---|---:|---:|---:|---|',
  ...rows.map(row => `| ${row.name} | ${row.type} | ${row.input} | ${row.output} | ${row.saved}% | ${row.strategy} |`),
  '',
  'Generated by `npm run benchmark`.',
].join('\n');

writeFileSync('BENCHMARK.md', markdown + '\n', 'utf8');
console.log(markdown);
