#!/usr/bin/env tsx
import { mkdtempSync, rmSync, writeFileSync } from 'fs';
import { tmpdir } from 'os';
import { join } from 'path';
import { performance } from 'perf_hooks';
import { compress } from '../src/compression/strategies.js';
import { DEFAULT_CONFIG } from '../src/config/defaults.js';
import { AppState } from '../src/state/app-state.js';

const fixtures = [
  {
    name: 'Large JSON',
    input: JSON.stringify(
      Array.from({ length: 500 }, (_, i) => ({
        id: i,
        value: `item-${i}`,
        flags: ['alpha', 'beta', 'gamma'],
      }))
    ),
  },
  {
    name: 'Large Log',
    input:
      Array.from(
        { length: 1000 },
        (_, i) => `2026-03-16T10:00:${String(i % 60).padStart(2, '0')}Z INFO request ${i}`
      ).join('\n') + '\n2026-03-16T10:02:00Z ERROR database timeout',
  },
  {
    name: 'Git Diff',
    input: [
      'diff --git a/src/app.ts b/src/app.ts',
      '@@ -1,0 +1,6 @@',
      '+export function main() {',
      '+  console.log("hello");',
      '+}',
      'diff --git a/src/cache.ts b/src/cache.ts',
      '@@ -10,2 +10,5 @@',
      '-const oldCache = true;',
      '+const hotCache = true;',
      '+export const HOT_CACHE_LIMIT = 32;',
    ].join('\n'),
  },
];

const rows = fixtures.map(fixture => {
  const result = compress(fixture.input, { maxOutputChars: 2048, strategy: 'ultra' });
  return {
    name: fixture.name,
    type: result.contentType,
    input: fixture.input.length,
    output: result.output.length,
    saved: result.savedPercent,
    strategy: result.strategy,
  };
});

type StorageProfile = {
  name: string;
  hotCacheMB: number;
  hotCacheEntries: number;
  hotCacheTtlMs: number;
  cleanupEveryWrites: number;
};

type WorkflowScenario = {
  name: string;
  handleCount: number;
  hotSet: number;
  reads: number;
};

const originalStorage = structuredClone(DEFAULT_CONFIG.storage);

function formatCacheBytes(bytes: number): string {
  return `${(bytes / 1024).toFixed(1)} KB`;
}

function runWorkflowBenchmark(profile: StorageProfile, scenario: WorkflowScenario) {
  const stateDir = mkdtempSync(join(tmpdir(), `kcm-bench-${profile.name}-`));
  Object.assign(DEFAULT_CONFIG.storage, {
    ...originalStorage,
    stateDir,
    hotCacheMB: profile.hotCacheMB,
    hotCacheEntries: profile.hotCacheEntries,
    hotCacheTtlMs: profile.hotCacheTtlMs,
    cleanupEveryWrites: profile.cleanupEveryWrites,
  });

  const state = new AppState();
  const payload = 'x'.repeat(18_000);
  const handleIds: string[] = [];

  try {
    for (let i = 0; i < scenario.handleCount; i += 1) {
      handleIds.push(state.saveHandle(`${payload}-${i}`, `file-${i}.ts`).id);
    }

    const start = performance.now();
    for (let i = 0; i < scenario.reads; i += 1) {
      state.getHandle(handleIds[i % scenario.hotSet]);
    }
    const elapsedMs = performance.now() - start;
    const stats = state.getCacheStats();

    return {
      profile: profile.name,
      scenario: scenario.name,
      opsPerSec: Math.round(scenario.reads / (elapsedMs / 1000)),
      hits: stats.hits,
      misses: stats.misses,
      cacheBytes: formatCacheBytes(stats.bytes),
    };
  } finally {
    state.close();
    rmSync(stateDir, { recursive: true, force: true });
  }
}

const profiles: StorageProfile[] = [
  {
    name: 'lean',
    hotCacheMB: 2,
    hotCacheEntries: 16,
    hotCacheTtlMs: 5 * 60 * 1000,
    cleanupEveryWrites: 25,
  },
  {
    name: 'balanced',
    hotCacheMB: DEFAULT_CONFIG.storage.hotCacheMB,
    hotCacheEntries: DEFAULT_CONFIG.storage.hotCacheEntries,
    hotCacheTtlMs: DEFAULT_CONFIG.storage.hotCacheTtlMs,
    cleanupEveryWrites: DEFAULT_CONFIG.storage.cleanupEveryWrites,
  },
  {
    name: 'wide',
    hotCacheMB: 12,
    hotCacheEntries: 96,
    hotCacheTtlMs: 10 * 60 * 1000,
    cleanupEveryWrites: 100,
  },
];

const scenarios: WorkflowScenario[] = [
  { name: 'hot-set-48', handleCount: 48, hotSet: 48, reads: 6000 },
  { name: 'overflow-96', handleCount: 96, hotSet: 96, reads: 6000 },
];

const workflowRows = profiles.flatMap(profile =>
  scenarios.map(scenario => runWorkflowBenchmark(profile, scenario))
);
Object.assign(DEFAULT_CONFIG.storage, originalStorage);

const markdown = [
  '# Kanso Compression Benchmarks',
  '',
  '| Fixture | Type | Input Bytes | Output Bytes | Saved | Strategy |',
  '|---|---|---:|---:|---:|---|',
  ...rows.map(
    row =>
      `| ${row.name} | ${row.type} | ${row.input} | ${row.output} | ${row.saved}% | ${row.strategy} |`
  ),
  '',
  '## Development Workflow Benchmarks',
  '',
  'Local workflow probes for repeated `context://` handle reads. Higher ops/sec is better; the balanced profile is the current default.',
  '',
  '| Profile | Scenario | Ops/sec | Hits | Misses | Cache Footprint |',
  '|---|---|---:|---:|---:|---|',
  ...workflowRows.map(
    row =>
      `| ${row.profile} | ${row.scenario} | ${row.opsPerSec} | ${row.hits} | ${row.misses} | ${row.cacheBytes} |`
  ),
  '',
  'Generated by `npm run benchmark`.',
].join('\n');

writeFileSync('BENCHMARK.md', markdown + '\n', 'utf8');
console.log(markdown);
